#include "stm32f4xx_hal.h"
#include "i2c-lcd.h"

ADC_HandleTypeDef hadc1;  // Gestionnaire d'ADC
I2C_HandleTypeDef hi2c1;
void SystemClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_ADC1_Init(void);
static void MX_I2C1_Init(void);
uint32_t Read_ADC(void);
void Display_Water_Level(uint32_t adc_value);

int main(void) {
    HAL_Init();
    SystemClock_Config();

    MX_GPIO_Init();
    MX_ADC1_Init();
    MX_I2C1_Init();
    lcd_init();

    while (1) {
        uint32_t adc_value = Read_ADC();  // Lire la valeur ADC
        Display_Water_Level(adc_value);  // Afficher le niveau d'eau en pourcentage
        HAL_Delay(1000);  // Mettre à jour toutes les secondes
    }
}

// Fonction pour lire la valeur ADC depuis PA0
uint32_t Read_ADC(void) {
    HAL_ADC_Start(&hadc1);  // Démarrer l'ADC
    HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY);  // Attendre la conversion
    uint32_t adc_value = HAL_ADC_GetValue(&hadc1);  // Obtenir la valeur ADC
    HAL_ADC_Stop(&hadc1);  // Arrêter l'ADC
    return adc_value;
}

// Fonction pour afficher le niveau d'eau en pourcentage
void Display_Water_Level(uint32_t adc_value) {
    // Supposons que l'ADC ait une résolution de 12 bits (0-4095)
    // Et que la plage de niveau d'eau soit 0 à 100%
    uint32_t percentage = (adc_value * 100) / 4095;  // Convertir en pourcentage

    lcd_clear();  // Effacer l'écran LCD
    lcd_put_cur(0, 0);  // Positionner le curseur à la première ligne
    lcd_send_string("Niveau d'eau: ");  // Afficher le texte
    lcd_put_cur(1, 0);  // Positionner le curseur à la deuxième ligne
    char buffer[16];
    sprintf(buffer, "%lu %%", percentage);  // Formater la chaîne de caractères
    lcd_send_string(buffer);  // Afficher le pourcentage
}

